                <h3>Who's ready to start thinking about camp?!?</h3>
                <p>It's 2017, and the winds of change are a-blowin'. New leadership is taking over at CLC Youth Camp this year, as camp will be headed up by Jonathan Gamble and Jocelyn Gamble. Due to the fact that we have been consistently reaching capacity, and an inability to run two camp sessions this summer, we will be running one camp session for grades 6–9 from June 18–24. We cannot wait!</p>
                
                <h3>So what do you do at Camp?</h3>
                <p>Crafts, field games, FOB, survival classes, open gym dance parties, ninja, devotions, dodgeball, loud games of Bunco, jewelry-making, swimming, kickball, lightning, music sing-a-longs (a camp favorite!), campfires, s'mores, skits and so much more! CLC Youth Camp offers an opportunity for our campers to try new activities, develop new skills and expand their outdoor exposure, all in Christian fellowship with devoted leadership.</p>
                
                <h3>What is all this talk about UTC?</h3>
                <p>Tennis balls and slingshots. Giant slip and slide and swim suits. Gross slime. These momentous occasions can mean only one thing — ULTIMATE TEAM COMPETITION. Counselors both plan and implement massive team events in which campers of all ages work together to achieve their goal — whether it's shooting tennis balls across an open field or sludging through hills of slime. It's messy, dirty, frustrating … and a camper favorite (makes for some good pictures too!).</p>

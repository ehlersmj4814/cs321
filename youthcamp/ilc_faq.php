                <h3><strong>Q: Where is camp held?</strong></h3>
                <br>
                A: Camp is help on the campus of <a href="http://www.ilc.edu" target="_blank">Immanuel Lutheran College</a> in Eau Claire, WI. The facility has a boys dorm, girls dorm, kitchen and dining facilities, gym, classrooms and sports fields. The campus is near a creek and woods with great paths for hiking.
            <h3>
                <strong>Q: Who will be supervising my child?</strong></h3>
                <br>
                A: Camp is headed up by directors Jonathan Gamble and Jocelyn Gamble. <!--Both directors have six years of experience directing previous camps at ILC and Koyquin.--> The <a href="ilc_staff.php">camp staff</a> is picked from numerous volunteers who were previous campers or who love working with children. All counselors, kitchen staff, and other helpers are role models for your child, providing spiritual leadership while enjoying the week of summer camp. Most counselors have volunteered at either or both previously mentioned camps and are well equipped for the week. Many are certified in CPR and First Aid. There is also a full time registered nurse, a kitchen manager and a pastor on site.
            
            <h3>
                <strong>Q: What should we pack?</strong>
                <br></h3>
                A: Here is a suggested <a href="packing_list.pdf" target="_blank">packing list</a>. We always end up with quite a pile of clothes at the end of camp, DEFINITELY make sure that you put your name on all your belongings. We want you to bring home everything that you came with!
            
            <h3>
                <strong>Q: What if my child becomes homesick?</strong></h3>
                <br>
                A: This is common, especially during the first couple of days. We have a busy schedule planned for our campers to keep their minds off home. Our staff is great with interacting with the children and taking note of those who may be missing home. We will help your child in any way we can, and in the worst case scenario, make arrangements to help them get home if the week is too unbearable. We have a great group of friendly campers who definitely help out with homesickness, too!
            
            <h3>
                <strong>Q: Is my child ready for camp, or should I wait a year?</strong></h3>
                <br>
                A: Camp gives your child an opportunity to meet peers and role models with the same foundation in Christ. The friends your child will make and the things he/she will learn will aid in preparing him/her for challenges ahead: middle school, high school, friendships, etc. It is a unique opportunity for fellowship with others outside their school, home, and church environment. Only you as a parent can decide if your child is the right age, but we definitely encourage you to allow them to attend. This age (5th grade) has proven to be very appropriate in past camps.
            
            <h3>
                <strong>Q: Who are the spiritual leaders?</strong></h3>
                <br>
                A: The religion classes and chapels will be given by a pastor and laypeople. We consider each member of the camp staff to be a spiritual leader. Our staff is full of CLC members who let their light shine, who have the Lord at the center of their hearts.
            
            <h3>
                <strong>Q: Can I contact my child during the week?</strong></h3>
                <br>
                A: We will send out contact numbers for the commons and kitchen (staffed continuously throughout the week) and also for the directors to the parents of all registered campers. If you would like to send a cell phone with your child, that will be allowed, but your child will be required to check in his/her phone upon arrival to prevent damage and loss.
           
            <h3>
                <strong>Q: My child is shy, will he/she make friends?</strong></h3>
                <br>
                A: Absolutely! Your child will not be left alone to fend for themselves. The camp staff knows the majority of the returning campers which allows them to spend time getting to know the new campers and introducing them to others. We plan a busy schedule which includes active participation for all campers which makes it hard to be lonely and creates fun for everyone!
        
            <h3>
                <strong>Q: Is there more to camp than sports?</strong></h3>
                <br>
                A: Much more! The day starts with calisthenics, followed by chapel and Bible classes. Activity periods follow (sports, crafts, swimming, etc.) with room for a couple meals in between. Nightly activities include singing, free time and specially planned fun activities.
        

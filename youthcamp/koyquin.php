        <h3>Who's ready to start thinking about camp?!?</h3>
        <p>Camp Koyquin is back this summer in Pillager, MN and we couldn't be more excited! You're invited to a week filled with Christ, fellowship, friends, Sharpie tattoos, and fun in the sun. Live life on the wild side as you skid down the wooden slide and sail across the water, learn to kayak, canoe, line dance, and even go fishing on the pontoon. Arts and Crafts are always a favorite whether we are weaving your own parachute cord survival bracelets or crafting the perfect marshmallow gun. New activities and programming is in the works! During the week of camp, parents will be able to keep up with our daily activities by visiting our online blog!</p>

        <h3>How do we get to Camp?</h3>
        <p>Need a ride to camp? You can take the bus! Boarding/Drop-off locations include Messiah in Eau Claire, WI or Berea in Inver Grove Heights, MN. The cost is $60 per camper which includes a meal each way. To reserve your spot, please complete and return the Charter Bus Transportation form. Reservations for your spot on the Blackhawk Express Charter Bus will be made first come, first serve! </p>

        <h3>What is all at Camp Koyquin?</h3>
        <p>Camp Koyquin offers an opportunity for our campers to try new activities, develop new skills, and expand their outdoor exposure, all in Christian fellowship with devoted leadership. In addition to classic camp activities such as sports, swimming, and devotions, we offer unique programs designed to enhance learning and experiences. Some of this year's programming will include: fishing instruction, small group Bible classes, and a few surprises.</p>

        <p>Tennis balls and slingshots. Giant water wheels and massive, paint covered capture the flag battles. These momentous occasions can mean only one thing — Ultimate Team Competition! Counselors both plan and implement huge team events in which campers of all ages work together to achieve their goal — whether it be shooting tennis balls across an open field or sludging through hills of slime. It's messy, dirty, frustrating… and a camper favorite (makes for some good pictures too!).</p>
        
        <p>Because we have a full time nurse on staff, we are able to allow your kids to ride on the WOODEN SLIDE OF TERROR, only one fixture of our waterfront program. Kayaking, paddle boating, water trampolines, diving docks, canoeing, and fishing are common activities at Camp Koyquin, and of course lots of swimming too!.</p>
